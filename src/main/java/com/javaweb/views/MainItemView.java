package com.javaweb.views;

public class MainItemView {
	private String mainitemid;
	private String projName;
	private String itemprice;
	private String mainflag;
	private String projcategoryid;
	private String categoryname;

	public String getMainitemid() {
		return mainitemid;
	}

	public void setMainitemid(String mainitemid) {
		this.mainitemid = mainitemid;
	}

	public String getProjName() {
		return projName;
	}

	public void setProjName(String projName) {
		this.projName = projName;
	}

	public String getItemprice() {
		return itemprice;
	}

	public void setItemprice(String itemprice) {
		this.itemprice = itemprice;
	}

	public String getMainflag() {
		return mainflag;
	}

	public void setMainflag(String mainflag) {
		this.mainflag = mainflag;
	}

	public String getProjcategoryid() {
		return projcategoryid;
	}

	public void setProjcategoryid(String projcategoryid) {
		this.projcategoryid = projcategoryid;
	}

	public String getCategoryname() {
		return categoryname;
	}

	public void setCategoryname(String categoryname) {
		this.categoryname = categoryname;
	}

}
